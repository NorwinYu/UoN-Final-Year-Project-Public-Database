package wikipedia.model;

public class WikipediaUser {
	
	private String id;
	private String name;
	
	public WikipediaUser() {
	
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
